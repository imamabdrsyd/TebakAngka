package latihanbasic;

public class DoWhileTest {

    public static void main(String[] args) {
        int x = -1;
        while (x < 0){
            System.out.println("Hai");
            x++;
        }
        int y = -1;
        do {
            System.out.println("Aku Do While");
            y++;
        } while (y < 0);   
    }
    
}
